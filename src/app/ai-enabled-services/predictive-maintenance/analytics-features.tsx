// Feature Item Component
function AnalyticsFeatureItem({
  icon,
  title,
}: {
  icon: string;
  title: string;
}) {
  const IconComponent =
    icon === "sidebar-left"
      ? SidebarLeft
      : icon === "dashboard-speed"
      ? DashboardSpeed
      : icon === "notification-square"
      ? NotificationSquare
      : icon === "alert"
      ? Alert
      : icon === "database-sync"
      ? DatabaseSync
      : VuesaxLinearKanban;

  const iconWrapperClass =
    icon === "database-sync"
      ? "bg-[#f0f0f0] box-border content-stretch flex gap-[10px] items-center overflow-clip p-[19px] relative rounded-[12px] shrink-0"
      : "bg-[#f0f0f0] box-border content-stretch flex gap-[10px] items-center justify-center overflow-clip p-[18px] relative rounded-[12px] shrink-0 size-[66px]";

  return (
    <div className="content-stretch flex gap-[24px] items-center relative shrink-0 w-[311px]">
      <div className={iconWrapperClass}>
        {icon === "database-sync" ? <IconComponent /> : <IconComponent />}
      </div>
      <p className="basis-0 font-['Gilroy:Medium',sans-serif] grow leading-[26px] min-h-px min-w-px not-italic relative shrink-0 text-[#282828] text-[20px]">
        {title}
      </p>
    </div>
  );
}

